#ifndef TYPES_H
#define TYPES_H

/*
 * types.h
 *
 * Types/aliases we want present every source file
 */

/*
 * Fixed-width numeric types
 */
#include <stdint.h>
#include <inttypes.h>

typedef uint8_t  u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;

typedef int8_t  i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;

typedef uint16_t f16;
typedef float    f32;
typedef double   f64;


#endif
